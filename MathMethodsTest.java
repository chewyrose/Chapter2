public class MathMethodsTest {
    public static void main(String[] args) {
        // 1. abs() - Returns the absolute value
        System.out.println("Absolute value of -10: " + Math.abs(-10));

        // 2. sqrt() - Returns the square root
        System.out.println("Square root of 25: " + Math.sqrt(25));

        // 3. pow() - Returns the value of the first argument raised to the power of the second argument
        System.out.println("2 raised to the power 3: " + Math.pow(2, 3));

        // 4. max() - Returns the maximum of two values
        System.out.println("Maximum of 10 and 20: " + Math.max(10, 20));

        // 5. random() - Returns a random double between 0.0 and 1.0
        System.out.println("Random value between 0.0 and 1.0: " + Math.random());
    }
}
